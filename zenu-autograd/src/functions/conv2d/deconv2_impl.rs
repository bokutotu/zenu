fn get_deconv_outsize(size: usize, k: usize, s: usize, p: usize) -> usize {
    s * (size - 1) + k - 2 * p
}

pub(crate) fn deconv2d_inner<T: Num>(
    img: Matrix<ViewMem<T>, DimDyn>,
    kernel: Matrix<ViewMem<T>, DimDyn>,
    padding: (usize, usize),
    stride: (usize, usize),
) -> Matrix<OwnedMem<T>, DimDyn> {
    let (batch_size, c, h, w) = img.shape();
    let kernel_shape = kernel.shape().slice();
    let (oc, ic, kh, kw) = (
        kernel_shape[0],
        kernel_shape[1],
        kernel_shape[2],
        kernel_shape[3],
    );
    let (ph, pw) = padding;
    let (sh, sw) = stride;

    let (out_h, out_w) = (
        get_deconv_outsize(h, kh, sh, ph),
        get_deconv_outsize(w, kw, sw, pw),
    );
}

